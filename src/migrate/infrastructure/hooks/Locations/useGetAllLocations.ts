import { useCallback, useState } from "react";
import { locationsService } from "../../../domain/services/Locations.service";

export const useGetAllLocations = () => {
  const [data, setData] = useState<any>(undefined);
  const [error, setError] = useState<undefined>(undefined);
  const [isLoading, setIsLoading] = useState(false);

  const get = useCallback(async () => {
    setIsLoading(true);
    try {
      const newLocations = await locationsService.getAlllocations();

      setData(newLocations);
    } catch (e: any) {
      setError(e);
      console.error(e);
    }
    setIsLoading(false);
  }, [setData, setIsLoading]);

  return { data, error, isLoading, get };
};
